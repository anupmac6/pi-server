exports.getBiblePlanEmailTemplate = (biblePlan, scheduleData) => {
  //* Header
  const heading = getBiblePlanHeading();
  //* Day Information
  const dayInformation = getBiblePlanDayInformation(biblePlan.currentDay);
  //* Timeline Period
  const timelinePeriod = getBiblePlanTimelinePeriod(
    scheduleData.schedule.period,
    "hello"
  );
  //* First Reading
  const firstReading = getBiblePlanFirstReading(
    scheduleData.schedule.firstReading.verse,
    scheduleData.firstReadingData.verse
  );
  //* Second Reading
  const secondReading = getBiblePlanSecondReading(
    scheduleData.schedule.secondReading.verse,
    scheduleData.secondReadingData.verse
  );
  //* Psalm
  const psalm = getBiblePlanPsalm(
    scheduleData.schedule.secondReading.verse,
    scheduleData.schedule.psalm.verse,
    scheduleData.psalmData.verse
  );
  //* Thank you note
  const thankYou = getBiblePlanThankYouNote();
  //* Footer
  const footer = getBiblePlanFooter();
  //* Merge the content
  const content =
    heading +
    dayInformation +
    timelinePeriod +
    firstReading +
    secondReading +
    psalm +
    thankYou +
    footer;
  //* Wrap everything in email
  const email = wrapBiblePlanContentInEmail(content);

  return email;
};
